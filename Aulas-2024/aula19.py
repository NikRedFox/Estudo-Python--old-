"""
Operadores de comparação (relacionais)
OP      Significado           Exemplo(True)
>       Maior                 2 > 1
>=      Maior ou igual        2 >= 2
<       Menor                 1 < 2
<=      Menor ou igual        2 <= 2
==      Igual                 'a' == 'a'
!=      Diferente             'a' != 'b'
"""
maior = 2 > 1
maoi = 2 >= 2
menor = 1 < 2
meoi = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print(maior)
print(maoi)
print(menor)
print(meoi)
print(igual)
print(diferente)
