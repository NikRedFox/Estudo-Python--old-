# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas possiveis:
# Sim (true) ou não (False).
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador logico que questiona se um valor é igual a outro.
print(10 == 10)  # Sim -> True
print(0 == 11)   # Não -> False
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))

